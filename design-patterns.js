/*

 Types of Design Patterns

    Creational patterns: 

    These kind of patterns focus on ways to create objects. When creating objects in 
    large applications, the tendency to make things complex is always there. 
    Creational design patterns solve this problem by controlling the object creation.

    Structural patterns: 
    Structural patterns provide ways to manage relationships between objects and 
    also create class structure. One way this is achieved is by using inheritance 
    and composition to create a large object from small objects.

    Behavioral patterns: 
    Behavioral patterns are patterns that focus on interaction between objects. Where 
    creational patterns describe a moment of time and structural patterns describe a 
    more or less static structure, behavioral patterns describe a process or a flow.


*/